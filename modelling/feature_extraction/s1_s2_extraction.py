extract_nlcd_fn    = make_extractor(nlcd_canopy,   scale=30)

def extract_gedi_buffered(feature):
    """
    GEDI has sparse orbital coverage — a point extractor returns null
    for most plots. Buffer 500m and take mean of any shots within range.
    If still null (no shots within 500m), returns null gracefully.
    """
    buffered = feature.geometry().buffer(500)
    values   = gedi.reduceRegion(
                   reducer=ee.Reducer.mean(),
                   geometry=buffered,
                   scale=25,
                   maxPixels=1e9)
    return feature.set(values)

