# 5. SENTINEL-2 — SPECTRAL INDICES + RAW BANDS
# ============================================================
def calculate_indices(image):
    """
    25 spectral indices across Structure / Physiology / Biochemistry.
    Raw bands B2-B8A, B11, B12 retained automatically via image.addBands.
    All NIR references use B8A (narrow NIR, 20m).
    """
    # --- STRUCTURE ---
    NDVI  = image.expression('(NIR-RED)/(NIR+RED)',
              {'NIR':image.select('B8A'),'RED':image.select('B4')}).rename('NDVI')
    ARVI  = image.expression('(NIR-(2*RED-BLUE))/(NIR+(2*RED-BLUE))',
              {'NIR':image.select('B8A'),'RED':image.select('B4'),
               'BLUE':image.select('B2')}).rename('ARVI')
    EVI7  = image.expression('2.5*(RE3-RED)/(1+RE3+6*RED-7.5*BLUE)',
              {'RE3':image.select('B7'),'RED':image.select('B4'),
               'BLUE':image.select('B2')}).rename('EVI7')
    EVI8  = image.expression('2.5*(NIR-RED)/(1+NIR+6*RED-7.5*BLUE)',
              {'NIR':image.select('B8A'),'RED':image.select('B4'),
               'BLUE':image.select('B2')}).rename('EVI8')
    MSR   = image.expression('((RE3/RED)-1)/sqrt((RE3/RED)+1)',
              {'RE3':image.select('B7'),'RED':image.select('B4')}).rename('MSR')
    SAVI  = image.expression('1.5*(NIR-RED)/(NIR+RED+0.5)',
              {'NIR':image.select('B8A'),'RED':image.select('B4')}).rename('SAVI')
    WDRVI = image.expression('((0.01*RE3)-RED)/((0.01*RE3)+RED+0.98)',
              {'RE3':image.select('B7'),'RED':image.select('B4')}).rename('WDRVI')

    # --- PHYSIOLOGY / STRESS ---
    ARI1  = image.expression('(1/GREEN)-(1/RE1)',
              {'GREEN':image.select('B3'),'RE1':image.select('B5')}).rename('ARI1')
    ARI2  = image.expression('(NIR/GREEN)-(NIR/RE1)',
              {'NIR':image.select('B8A'),'GREEN':image.select('B3'),
               'RE1':image.select('B5')}).rename('ARI2')
    CRI1  = image.expression('(1/BLUE)-(1/GREEN)',
              {'BLUE':image.select('B2'),'GREEN':image.select('B3')}).rename('CRI1')
    CRI2  = image.expression('(1/BLUE)-(1/RE1)',
              {'BLUE':image.select('B2'),'RE1':image.select('B5')}).rename('CRI2')
    NDII11= image.expression('(NIR-SWIR1)/(NIR+SWIR1)',
              {'NIR':image.select('B8A'),'SWIR1':image.select('B11')}).rename('NDII11')
    NDII12= image.expression('(NIR-SWIR2)/(NIR+SWIR2)',
              {'NIR':image.select('B8A'),'SWIR2':image.select('B12')}).rename('NDII12')
    PSRI  = image.expression('(RED-GREEN)/NIR',
              {'RED':image.select('B4'),'GREEN':image.select('B3'),
               'NIR':image.select('B8A')}).rename('PSRI')

    # --- CHLOROPHYLL / BIOCHEMISTRY ---
    GCI   = image.expression('(NIR/GREEN)-1',
              {'NIR':image.select('B8A'),'GREEN':image.select('B3')}).rename('GCI')
    Clre  = image.expression('(RE3/RE1)-1',
              {'RE3':image.select('B7'),'RE1':image.select('B5')}).rename('Clre')
    GARI  = image.expression(
              '((NIR-GREEN)-(BLUE-RED))/((NIR+GREEN)-(BLUE-RED))',
              {'NIR':image.select('B8A'),'GREEN':image.select('B3'),
               'BLUE':image.select('B2'),'RED':image.select('B4')}).rename('GARI')
    GNDVI = image.expression('(NIR-GREEN)/(NIR+GREEN)',
              {'NIR':image.select('B8A'),'GREEN':image.select('B3')}).rename('GNDVI')
    IRECI = image.expression('(RE2/RE1)*(RE3-RED)',
              {'RE2':image.select('B6'),'RE1':image.select('B5'),
               'RE3':image.select('B7'),'RED':image.select('B4')}).rename('IRECI')
    MCARI = image.expression('1-(0.2*(RE1-GREEN)/(RE1-RED))',
              {'RE1':image.select('B5'),'GREEN':image.select('B3'),
               'RED':image.select('B4')}).rename('MCARI')
    TCARI = image.expression('3*(RE1-RED)-0.2*(RE1-GREEN)*(RE1/RED)',
              {'RE1':image.select('B5'),'RED':image.select('B4'),
               'GREEN':image.select('B3')}).rename('TCARI')
    NDVIRE= image.expression('(NIR-RE2)/(NIR+RE2)',
              {'NIR':image.select('B8A'),'RE2':image.select('B6')}).rename('NDVIRE')
    NDI45 = image.expression('(RE1-RED)/(RE1+RED)',
              {'RE1':image.select('B5'),'RED':image.select('B4')}).rename('NDI45')
    S2REP = image.expression('705+35*((((RE3+RED)/2)-RE1)/(RE2-RE1))',
              {'RE3':image.select('B7'),'RE2':image.select('B6'),
               'RE1':image.select('B5'),'RED':image.select('B4')}).rename('S2REP')
    TVI   = image.expression('0.5*(120*(RE2-GREEN)-200*(RED-GREEN))',
              {'RE2':image.select('B6'),'RED':image.select('B4'),
               'GREEN':image.select('B3')}).rename('TVI')

    return image.addBands([
        NDVI, ARVI, EVI7, EVI8, MSR, SAVI, WDRVI,
        ARI1, ARI2, CRI1, CRI2, NDII11, NDII12, PSRI,
        GCI, Clre, GARI, GNDVI, IRECI, MCARI, TCARI,
        NDVIRE, NDI45, S2REP, TVI
    ])

