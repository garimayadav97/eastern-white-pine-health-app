print("Computing seasonal amplitude features...")

MONTHS  = list(MONTH_WINDOWS.keys())
INDICES = ['NDVI', 'EVI8', 'GNDVI', 'NDII11', 'S2REP',
           'IRECI', 'PSRI', 'SAVI']

amplitude_rows = []

for _, row in df_raw.iterrows():
    year = int(row['visit_year'])
    amp  = {}

    for idx in INDICES:
        monthly_vals = []
        for m in MONTHS:
            col = f"{idx}_{m}"
            if col in row and pd.notna(row[col]):
                monthly_vals.append((m, float(row[col])))

        if len(monthly_vals) >= 3:
            vals = [v for _, v in monthly_vals]
            amp[f'{idx}_season_max']  = max(vals)
            amp[f'{idx}_season_min']  = min(vals)
            amp[f'{idx}_season_amp']  = max(vals) - min(vals)
            amp[f'{idx}_season_mean'] = float(np.mean(vals))
            amp[f'{idx}_peak_month']  = monthly_vals[vals.index(max(vals))][0]
        else:
            for s in ['season_max','season_min','season_amp','season_mean','peak_month']:
                amp[f'{idx}_{s}'] = None

    # NDVI green-up rate (Apr→Jun slope)
    early = [float(row[f'NDVI_{m}']) for m in ['Apr','May','Jun']
             if f'NDVI_{m}' in row and pd.notna(row[f'NDVI_{m}'])]
    amp['NDVI_greenup_rate'] = (early[-1]-early[0])/len(early) if len(early)>=2 else None

    # NDVI senescence rate (Aug→Nov slope)
    late = [float(row[f'NDVI_{m}']) for m in ['Aug','Sep','Oct','Nov']
            if f'NDVI_{m}' in row and pd.notna(row[f'NDVI_{m}'])]
    amp['NDVI_senescence_rate'] = (late[-1]-late[0])/len(late) if len(late)>=2 else None

    amplitude_rows.append(amp)

df_amp = pd.DataFrame(amplitude_rows)
