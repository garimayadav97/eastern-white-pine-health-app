# 4. SENTINEL-2 — CLOUD MASKING (SCL only, no band thresholds)
# ============================================================
def mask_s2_clouds_scl(image):
    """
    Mask using SCL only:
      3  = cloud shadow
      8  = cloud medium probability
      9  = cloud high probability
      10 = cirrus
    Scale reflectance from DN (0-10000) to 0-1.
    """
    scl  = image.select('SCL')
    mask = (scl.neq(3)
               .And(scl.neq(8))
               .And(scl.neq(9))
               .And(scl.neq(10)))
    return image.updateMask(mask).divide(10000)

