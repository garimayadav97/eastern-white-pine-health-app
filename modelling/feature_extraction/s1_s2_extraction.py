# ============================================================
def extract_point(feature, image, prefix):
    """Extract all bands at point, prefix band names with month_year."""
    values   = image.reduceRegion(
                   reducer=ee.Reducer.first(),
                   geometry=feature.geometry(),
                   scale=10,
                   maxPixels=1e9)
    keys     = values.keys()
    new_keys = keys.map(lambda k: ee.String(k).cat('_').cat(prefix))
    return feature.set(ee.Dictionary.fromLists(new_keys, values.values(keys)))


def make_extractor(image, scale):
    """
    Closure factory — returns single-argument function for .map().
    No prefix; static layers use their own band names as-is.
    """
    def _extract(feature):
        values = image.reduceRegion(
                     reducer=ee.Reducer.first(),
                     geometry=feature.geometry(),
                     scale=scale,
                     maxPixels=1e9)
        return feature.set(values)
    return _extract


# Pre-built extractors for static layers
