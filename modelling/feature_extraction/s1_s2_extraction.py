# 6. SENTINEL-2 — BUILD MONTHLY COMPOSITES
# ============================================================
def build_s2_composites(date_ranges):
    composites = {}
    for month, (start, end) in date_ranges.items():
        col = (ee.ImageCollection('COPERNICUS/S2_SR_HARMONIZED')
               .filterDate(start, end)
               .filterBounds(combined_region)
               .filter(ee.Filter.lt('CLOUDY_PIXEL_PERCENTAGE', 40))
               .select(['B2','B3','B4','B5','B6','B7','B8','B8A','B11','B12','SCL'])
               .map(mask_s2_clouds_scl)
               .map(calculate_indices))
        n = col.size().getInfo()
        if n == 0:
            print(f"  [WARN] No S2 for {month} ({start}–{end})")
            composites[month] = None
        else:
            composites[month] = col.median().clip(combined_region)
            print(f"  [OK] S2 {month} ({start}–{end}): {n} scenes")
    return composites

