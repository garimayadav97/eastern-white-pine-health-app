# 7. SENTINEL-1 — BACKSCATTER + GLCM TEXTURES
# ============================================================
def preprocess_s1(date_range):
    """
    ASC-only median composite in dB.
    Derived dB metrics:
        VV_VH_Ratio = VV - VH
        Sqrt_VV_VH  = (VV-VH)/2
        VV_Times_VH = VV + VH
    GLCM textures on VV (window size=3):
        ASM, contrast, correlation, variance, homogeneity (IDM), entropy
    """
    s1_col = (ee.ImageCollection('COPERNICUS/S1_GRD')
              .filterDate(date_range[0], date_range[1])
              .filterBounds(combined_region)
              .filter(ee.Filter.eq('instrumentMode', 'IW'))
              .filter(ee.Filter.eq('orbitProperties_pass', 'ASCENDING'))
              .filter(ee.Filter.listContains('transmitterReceiverPolarisation', 'VV'))
              .filter(ee.Filter.listContains('transmitterReceiverPolarisation', 'VH'))
              .select(['VV', 'VH']))

    if s1_col.size().getInfo() == 0:
        print(f"  [WARN] No S1 ASC for {date_range}")
        return None

    composite   = s1_col.median().clip(combined_region)
    vv          = composite.select('VV')
    vh          = composite.select('VH')

    vv_vh_ratio = vv.subtract(vh).rename('VV_VH_Ratio')
    sqrt_vv_vh  = vv.subtract(vh).divide(2).rename('Sqrt_VV_VH')
    vv_times_vh = vv.add(vh).rename('VV_Times_VH')

    # GLCM: scale VV from dB (~-25 to 0) → 0-255 int required by glcmTexture
    vv_scaled = (vv.unitScale(-25, 0)
                   .multiply(255)
                   .toInt()
                   .rename('VV_scaled'))
    glcm = vv_scaled.glcmTexture(size=3).select([
        'VV_scaled_asm',
        'VV_scaled_contrast',
        'VV_scaled_corr',
        'VV_scaled_var',
        'VV_scaled_idm',
        'VV_scaled_ent',
    ])

    return (composite
            .addBands(vv_vh_ratio)
            .addBands(sqrt_vv_vh)
            .addBands(vv_times_vh)
            .addBands(glcm))

def build_s1_composites(date_ranges):
    composites = {}
    for month, date_range in date_ranges.items():
        print(f"  Processing S1 {month}...")
        composites[month] = preprocess_s1(date_range)
    return composites

